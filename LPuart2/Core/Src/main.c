/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_LPUART1_UART_Init();
  /* USER CODE BEGIN 2 */

  /* Disable RXNE interrupt - we use polling in main loop */
  LPUART1->CR1 &= ~USART_CR1_RXNEIE_RXFNEIE;
  HAL_NVIC_DisableIRQ(LPUART1_IRQn);

  /* Clear any pending flags */
  LPUART1->ICR = 0xFFFFFFFF;

  /* Power-on banner */
  HAL_UART_Transmit(&hlpuart1, (uint8_t*)"[Board2] Ready\r\n", 16, 1000);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */

    /* Poll RX: if RDR has data, read it into frame buffer */
    if (LPUART1->ISR & USART_ISR_RXNE_RXFNE)
    {
      uint8_t data = (uint8_t)(LPUART1->RDR & 0xFF);

      if (rx_idx < BUF_SIZE)
      {
        rx_buf[rx_idx++] = data;
      }
      rx_active = 1;
      last_rx_tick = HAL_GetTick();
    }

    /* Clear any error flags to prevent lockup */
    if (LPUART1->ISR & (USART_ISR_ORE | USART_ISR_FE | USART_ISR_NE | USART_ISR_PE))
    {
      LPUART1->ICR = USART_ICR_ORECF | USART_ICR_FECF | USART_ICR_NECF | USART_ICR_PECF;
    }

    /* Check if a frame has ended (no new byte for IDLE_TIMEOUT_MS) */
    if (rx_active && (HAL_GetTick() - last_rx_tick >= IDLE_TIMEOUT_MS))
    {
      uint16_t len = rx_idx;
      rx_idx = 0;
      rx_active = 0;

      if (len > 0 && len <= BUF_SIZE)
      {
        /* Copy frame and append '*' */
        memcpy(tx_buf, rx_buf, len);
        tx_buf[len] = '*';

        /* Send back */
        HAL_UART_Transmit(&hlpuart1, tx_buf, len + 1, 100);
      }
    }

  }
  /* USER CODE END 3 */
}

